package in.sli.main.beans.contact;

public class ColabRequest {
	private String name;
	private String role;
	private String projectIdeaDesc;
	private String skills;
	private String contactDetails;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getProjectIdeaDesc() {
		return projectIdeaDesc;
	}
	public void setProjectIdeaDesc(String projectIdeaDesc) {
		this.projectIdeaDesc = projectIdeaDesc;
	}
	public String getSkills() {
		return skills;
	}
	public void setSkills(String skills) {
		this.skills = skills;
	}
	public String getContactDetails() {
		return contactDetails;
	}
	public void setContactDetails(String contactDetails) {
		this.contactDetails = contactDetails;
	}
	
	
}
